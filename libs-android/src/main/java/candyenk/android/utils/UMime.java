    /**
     * 解析后缀输出MimeType[]
     */
    public static String[] ps(String... name) {
        List<String> list = new ArrayList<>(name.length);
        for (String s : name) {
            String ss = map.get(s);
            if (ss != null) list.add(ss);
        }
        return list.toArray(new String[0]);
    }

    /**
     * 解析后输出MimeType
     */
    public static String p(String name) {
        String ss = map.get(name);
        return ss != null ? ss : "";
    }

